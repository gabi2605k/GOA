# არითმეტიკული),იპოვეთ ყველა ლუწი რიცხვის ჯამი
def f(l):
    s = 0
    for i in l:
        s += i
        j = 0
    for i in range(0,s//len(l),2):
        j += i
    return j
print(f([12,61,12,621,353,16]))