# რაოდენობა და ასევე უნდა გაიგოთ სიაში მყოფი ინტეჯერების ჯამი
def f(l):
    s = 0
    k = 0
    for i in l:
        if type(i) == str :
            s += 1
        else:
            k+=i
    return k,s
print(f([12,"gabo",41,51]))