#  დააბრუნეთ ამ რიცხვის ჩათვლით ყველა ლუწი რიცხვის საშუალო არითმეტიკული
num = int(input())
def f(n):
    l = 0
    s = 0
    for i in range(1,n+1):
        if i % 2 == 0:
            s += 1
            l += i
    return l / s
print(f(num))