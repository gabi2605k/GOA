# 3) შექმენით ფუნქცია რომელსაც გადაეცემა მომხმარებლის შემოტანილი სახელი.
#  იმ შემთხვევაში თუ თქვენი სახელები ემთხვევა ქეის სენსიტიურობის დაიგნორების
#  შემთხვევაშიც, მაშინ დააბრუნოს: გამარჯობა {სახელი}!,
#  სასიამოვნოა თქვენი გაცნობა. სხვა შემთხვევაში გამარჯობა {სახელი}! 
name = input("Enter name:")
def f(nam):
    for i in nam:
        if i == i.capitalize():
            return nam +""+ "სასიამოვნოა თქვენი გაცნობა"
        else:
            return "გამარჯობა"+""+ nam
print(f(name))