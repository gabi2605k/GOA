# 2)ფუნქცია, რომელიც მიიღებს რიცხვს და დააბრუნებს "ლუწია" თუ ყველა ციფრი ლუწია, "კენტია" 
# თუ ყველა კენტია, ხოლო "შერეულია" თუ ორივე გვხვდება.
def f(ls):
    for i in ls:
        for k in i:
            if k % 2 == 0:
                return "ლუწია"
            elif k % 2 != 0:
                return "კენტია"
            else:
                return "შერეულია"
print(f(14613))