#  კენტ ინდექსზე მდგომი რიცხვების კვადრატების ჯამი
def f(list):
    r = []
    k = 0
    for i in list:
        if i % 2 == 0:
            r.append(i)
    for s in r:
        if r.index(s) % 2 != 0:
            k == k ** s
    return r , k
print(f([1,2,3,4,5,6,7,8]))