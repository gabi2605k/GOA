#  ამ სიიდან ყველაზე პატარა და ყველაზე დიდი რიცხვები
def f(list):
    max = 0
    low = 0
    for i in list:
        if max < i:
            max = i
        else:
            low = i
    return max , low
print(f([12,4,14]))