# მდგომი ელემენტების საშუალო არითმეტიკული
def f(l):
    res = []
    for i in range(len(l)):
        if i % 2 == 0:
            res.append(l[i])
    if len(res) > 0:
        return sum(res) / len(res)
print(f([14,15,61,23,61]))